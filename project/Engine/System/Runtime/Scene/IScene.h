#pragma once

//-----------------------------------------------------------------------------------------
// include
//-----------------------------------------------------------------------------------------
//* c++
#include <concepts>

////////////////////////////////////////////////////////////////////////////////////////////
// Interface Scene class
////////////////////////////////////////////////////////////////////////////////////////////
class IScene {
public:

	//=========================================================================================
	// public method
	//=========================================================================================

	virtual ~IScene() = default;

	virtual void Init() = 0;

	virtual void Update() = 0;

	virtual void Draw() = 0;

	virtual void Term() = 0;

};

////////////////////////////////////////////////////////////////////////////////////////////
// concept
////////////////////////////////////////////////////////////////////////////////////////////
template <class T>
concept DerivedFromScene = std::derived_from<T, IScene>;
