#include "Performance.h"

template <SecondsUnit T>
class DeltaTimePoint {
public:

	//=========================================================================================
	// public methods
	//=========================================================================================

	void AddDeltaTime() {
		*this += Performance::GetDeltaTime<T>();
	}

	void SubtractDeltaTime() {
		*this -= Performance::GetDeltaTime<T>();
	}

	//=========================================================================================
	// public operator
	//=========================================================================================



	//=========================================================================================
	// public member
	//=========================================================================================

	float time;

private:
};