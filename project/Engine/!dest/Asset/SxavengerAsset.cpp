#include "SxavengerAsset.h"

////////////////////////////////////////////////////////////////////////////////////////////
// namespace
////////////////////////////////////////////////////////////////////////////////////////////
namespace {
	static std::unique_ptr<AssetCollection> sAsset = nullptr;
}

////////////////////////////////////////////////////////////////////////////////////////////
// SxavengerAsset class methods
////////////////////////////////////////////////////////////////////////////////////////////

void SxavengerAsset::Init() {
	sAsset = std::make_unique<AssetCollection>();
}

void SxavengerAsset::Term() {
	sAsset.reset();
}

std::weak_ptr<AssetFile> SxavengerAsset::Import(const std::filesystem::path& filepath) {
	return sAsset->Import(filepath);
}

AssetCollection* SxavengerAsset::GetAsset() {
	return sAsset.get();
}
